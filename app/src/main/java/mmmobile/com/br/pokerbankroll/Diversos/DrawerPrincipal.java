package mmmobile.com.br.pokerbankroll.Diversos;

public class DrawerPrincipal {

    private final String title;
    private final int icon;

    public DrawerPrincipal(String title, int icon) {
        this.title = title;
        this.icon = icon;
    }

    public String getTitle() {
        return this.title;
    }

    public int getIcon() {
        return this.icon;
    }

}